package com.trg.j19p2;

public class Person {

	int age;
	private String studentName;
	public void setAge(int age)
	{
		this.age=age;
	}
	public int getAge()
	{
		return this.age;
	}
	public void setStudentName(String studentName)
	{
		this.studentName=studentName;
	}
	public String getStudentName(){
		return this.studentName;
	}
}
